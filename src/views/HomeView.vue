      <p class="lead">群体智能源于对以蚂蚁、蜜蜂等为代表的社会性昆虫的群体行为的研究。区别于单个智能有限的感知决策能力，群体智能是一种共享的或者群体的智能，以及集结多个智能体的能力进行高效决策的一种过程。当前，群体智能研究已经上升为国家战略。2017年7月，中华人民共和国国务院发布的《新一代人工智能发展规划》中，明确指出“群体智能”是人工智能领域的一个新的研究方向。科技部启动的《科技创新2030“新一代人工智能”重大项目指南》中，也将“群体智能”列为人工智能领域的五大持续攻关方向之一。由此可见，研究群体智能算法及其潜在应用具有重要的现实意义。</p>
      
